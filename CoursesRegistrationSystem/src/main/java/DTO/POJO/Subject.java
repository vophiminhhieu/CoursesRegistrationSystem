package DTO.POJO;
public class Subject {
	private String id;
	private String name;
	private int numbersOfCredit;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumbersOfCredit() {
		return numbersOfCredit;
	}
	public void setNumbersOfCredit(int numbersOfCredit) {
		this.numbersOfCredit = numbersOfCredit;
	}
}
